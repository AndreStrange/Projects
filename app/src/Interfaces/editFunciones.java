    private void insertFuncionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertFuncionActionPerformed
                       
        try{
            
            Connection con = conexionsqlserver.getConnection();
            PreparedStatement ps = con.prepareStatement("Insert into FUNCIONES (duracion, hora_inicio, id_sala, id_pelicula) values (?, ?, ?, ?)");

            ps.setString(1, txtDuracion.getText());
            ps.setString(2, txtHora.getText());
            ps.setString(3, txtIdSala.getText());
            ps.setString(4, txtPelicula.getText());
            
            int res= ps.executeUpdate();
            
            if(res>0){
               JOptionPane.showMessageDialog(null, "Nueva función registrada");
                limpiarCaja();
           } else{
               JOptionPane.showMessageDialog(null, "Error al registrar función");
                limpiarCaja();               
           }
            
        }catch(SQLException e){
            
            JOptionPane.showMessageDialog(null, e.toString());
            
        }
        
        
    }//GEN-LAST:event_insertFuncionActionPerformed

