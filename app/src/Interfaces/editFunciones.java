    private void selectFuncionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectFuncionActionPerformed
        
        try{
            Connection con = conexionsqlserver.getConnection();           
            PreparedStatement ps = con.prepareStatement("SELECT * FROM FUNCIONES WHERE id_funcion=?");
            
           ps.setString(1, txtId.getText());

            rs=ps.executeQuery();
            
          if(rs.next()){
                
                txtDuracion.setText(rs.getString("duracion"));
                txtHora.setText(rs.getString("hora_inicio"));
                txtIdSala.setText(rs.getString("id_sala"));
                txtSala.setText(rs.getString("numero_sala"));
                txtPelicula.setText(rs.getString("id_pelicula"));
               
            }
            else{
                JOptionPane.showMessageDialog(null, "No existe una función con este ID");
            }
            
        }catch(Exception e){
            
        }   
        
    }//GEN-LAST:event_selectFuncionActionPerformed

    private void updateFuncionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateFuncionesActionPerformed
              
        
        try{
            Connection con = conexionsqlserver.getConnection();           
            PreparedStatement ps = con.prepareStatement("UPDATE FUNCIONES SET duracion=?, hora_inicio=?, id_sala=?, id_pelicula=? WHERE id_funcion=?");
            
            ps.setString(1, txtDuracion.getText());
            ps.setString(2, txtHora.getText());
            ps.setString(3, txtIdSala.getText());
            ps.setString(4, txtPelicula.getText());
            ps.setString(5, txtId.getText());

            
           int res = ps.executeUpdate();
            
            if(res>0){
               JOptionPane.showMessageDialog(null, "Función actualizada");
                limpiarCaja();
           } else{
               JOptionPane.showMessageDialog(null, "Error al actualizar función");
                limpiarCaja();               
           }
            
        }catch(SQLException e){
            
            JOptionPane.showMessageDialog(null, e.toString());
            
        }              
        
    }//GEN-LAST:event_updateFuncionesActionPerformed

    private void deleteFuncionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteFuncionActionPerformed
       
        try{
            Connection con = conexionsqlserver.getConnection();           
            PreparedStatement ps = con.prepareStatement("DELETE FROM FUNCIONES WHERE ID_FUNCION=?");
            
            ps.setString(1, txtId.getText());
                        
           int res = ps.executeUpdate();
            
            if(res>0){
               JOptionPane.showMessageDialog(null, "Función Eliminada");
                limpiarCaja();
           } else{
               JOptionPane.showMessageDialog(null, "Error al Eliminar función");
                limpiarCaja();               
           }
            
        }catch(SQLException e){
            
            JOptionPane.showMessageDialog(null, e.toString());
            
        }            
        
    }//GEN-LAST:event_deleteFuncionActionPerformed

